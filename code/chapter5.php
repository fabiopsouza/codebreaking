<div class="row mt-3">
	<div class="col-12">
        <h5 style="color: #39CCCC">Primeira Guerra Mundial</h5>
    </div>
	
	<div class="col-12">	
		<p class="text-justify">Em agosto de 1914, um dia depois da Grã-Bretanha declarar guerra à Alemanha, o navio Telconia arrebentou os cabos transatlânticos da Alemanha e assegurou que mensagens inimigas para os EUA fossem enviadas por rádio.</p>
		<p class="text-justify">No mesmo dia, o contra-almirante Henry Oliver, diretor de espionagem naval, já tinha uma pilha de mensagens interceptadas crescendo em sua mesa. Sir Alfred Ewing reconheceu imediatamente como mensagens navais alemãs e começou a pesquisar cifras. Recrutou quatro professores falantes de alemão. Russell Clarke, um entusiasta do radioamador, montou um posto de escuta em Hunstanton, foi o começo do serviço Y, que envolveu 14 postos e interceptou quase todas as mensagens alemãs.</p>
		<p class="text-justify">A princípio com pouco progresso, então tiveram uma dose de sorte, em 26 de agosto, o cruzador alemão SMS Magdeburg encalhou, a marinha russa recolheu o corpo de um oficial afogado que ainda segurava os livros de códigos navais alemães.</p>
		<p class="text-justify">Charles Rotter, principal especialista em alemão, percebeu que as mensagens tinham sido codificadas e depois supracifradas (cifradas novamente) com uma substituição monoalfabética simples.</p>
	</div>
</div>

<div class="row">	
	<div class="col-12">
		<h5 style="color: #39CCCC">Seção 25 da Divisão de Informações</h5>
	</div>
	
	<div class="col-12">
		<p class="text-justify">A equipe de Ewing estava se expandindo e em novembro de 1914 havia se mudado para a Sala 40 do Prédio Velho do Almirantado, onde ao lado havia uma sala de descanso onde decifradores com excesso de trabalho podiam descansar. Então, foi oficialmente designada Seção 25 da Divisão de Informações ou ID25, mas o nome Sala 40 permaneceu.</p>	
		<p class="text-justify">Em dezembro de 1914, os decifradores da Sala 40, descobriram um ataque que seria feito pelos alemães, eles prepararam uma defesa com o objetivo de encotrar a flotilha alemã e engajá-la em seu retorno, o alto-comando aceitou o risco de mortes de civis, para não permitir que os alemães soubessem que tinham decifrado o código.</p>
		<p class="text-justify">Em janeiro de 1915 a marinha britânica teve nova oportunidade, dessa vez conseguiu interceptar os atacantes e afundar um cruzador, além de avariar outros 2. A reputação da Sala 40 estava assegurada. </p>
		<p class="text-justify">Após esse evento, em fevereiro do mesmo ano, os alemães mudaram o código, mas agora os decifradores conheciam as palavras codificadas e a nova cifra foi resolvida. </p>
		<p class="text-justify">No ano seguinte, a marinha alemã mudava o código toda noite, mas os decifradores da Sala 40 ficaram tão bons que, às vezes, conseguiam descobrir o novo código as duas da madrugada.</p>
	</div>
</div>

<div class="row">	
	<div class="col-12 ">
		<h5 style="color: #39CCCC">Transposição colunar</h5>
	</div>
	
	<div class="col-12">
		<p class="text-justify">Os alemães continuaram usando o mesmo livro de códigos, mas supercifrado com transposição colunar. </p>
		<p class="text-justify">Para cifrar uma mensagem usando essa técnica, a mensagem é escrita em linhas de comprimento fixado pela palavra-chave. Por exemplo, a mensagem “precisamos entregar o projeto do curso” for cifrada usando a palavra-chave UBOATS, a tabela ficará assim: </p>
    </div>
</div>

<div class="row">
	<div class="col-12">
		<img src="view/static/images/colunar1.png"  alt="Colunar" class="img-fluid img-thumbnail">

		<p class="mt-3">As células que restarem na última linha recebem letras aleatórias. Em seguida, as colunas são reordenadas em ordem alfabética da palavra-chave.</p>

		<img src="view/static/images/colunar2.png"  alt="Colunar" class="img-fluid img-thumbnail">

		<p class="text-justify mt-3">A mensagem é transmitida coluna a coluna.</p>

		<p class="text-justify">CSGOOW RMRPOS EOERDO SNREUN IEAJCG PATOTR</p>
		<p class="text-justify">Para decifrar, inverte-se o processo e para confundir ainda mais a mensagem pode ser cifrada novamente usando o mesmo processo, mas mudando a palavra-chave.</p>
	</div>
</div>
	
<div class="row">
	<div class="col-12 ">	
		<h5 style="color: #39CCCC">O telegrama de Zimmermann</h5>
	</div>
	
	<div class="col-12">
		<p class="text-justify">Considerado com um dos eventos fundamentais da Primeira Guerra Mundial, a decodificação do Telegrama de Zimmermann, ocorreu em janeiro de 1917.</p>
		<p class="text-justify">Os alemães planejavam uma retomada na guerra que poderia levar os Estados Unidos de volta a guerra ao lado dos aliados e a Alemanha propunha uma aliança ao México, oferecendo um quantia financeira para ajudá-lo a reconquistar alguns territórios perdidos, dessa forma os soldados americanos não seriam enviados a Europa.</p>
		<p class="text-justify">As cópias dessa mensagem logo foram parar na Sala 40 e foi decifrada completamente, mas a questão era o que fazer com a mensagem? </p>
		<p class="text-justify">Os britânicos não gostariam de revelar que eram capazes de decifrar as mensagens e nem que interceptavam mensagens com países neutros. Então o embaixador britânico no México através de suborno conseguiu uma cópia da mensagem, com uma criptografia simples que foi divulgada em jornais americanos. Em abril de 1971, os Estados Unidos declararam guerra à Alemanha.</p>                
