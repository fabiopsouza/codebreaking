<div class="row">
    <div class="col-12">
        <p class="text-justify">"Cerca de uma quinzena depois, o comandante me mandou chamar e, em silêncio, me entregou um telegrama do War Office: ‘O tenente-intérprete Toye deve se apresentar assim que possível ao Almirantado para uma missão especial.’ Tão onipotente e expedito é o Almirantado britânico quando sua mente finalmente se decide; pense nas jardas de fita vermelha que devem ter sido cortadas naquelas duas semanas!”</i> <b>Frank Toye</b>, decifrador, Sala 40.</p>
        <p class="text-justify">Durante a Primeria Guerra Mundial, o rádio assumiu a primazia como meio de comunicação mais flexível, mas as mensagens pelo rádio eram ainda mais fáceis de interceptar do que as enviadas pelo telégrafo. Os britânicos criaram uma rede de postos de escuta para interceptar mensagens alemãs oficiais, remetidas então ao Almirantado para serem decifradas.</p>
