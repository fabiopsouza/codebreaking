        <h5>Primeira Guerra Mundial</h5><br />
    </div>
</div>

<div class="row">	
	<div class="col-12 col-md-12">	
		<p class="text-justify">Em agosto de 1914, um dia depois da Grã-Bretanha declarar guerra à Alemanha, o navio Telconia arrebentou os cabos transatlânticos da Alemanha e assegurou que mensagens inimigas para os EUA fossem enviadas por rádio.</p>
		<p class="text-justify">No mesmo dia, o contra-almirante Henry Oliver, diretor de espionagem naval, já tinha uma pilha de mensagens interceptadas crescendo em sua mesa. Sir Alfred Ewing reconheceu imediatamente como mensagens navais alemãs e começou a pesquisar cifras. Recrutou quatro professores falantes de alemão. Russell Clarke, um entusiasta do radioamador, montou um posto de escuta em Hunstanton, foi o começo do serviço Y, que envolveu 14 postos e interceptou quase todas as mensagens alemãs.</p>
		<p class="text-justify">A princípio com pouco progresso, então tiveram uma dose de sorte, em 26 de agosto, o cruzador alemão SMS Magdeburg encalhou, a marinha russa recolheu o corpo de um oficial afogado que ainda segurava os livros de códigos navais alemães. </p>
		<p class="text-justify">Charles Rotter, principal especialista em alemão, percebeu que as mensagens tinham sido codificadas e depois supracifradas (cifradas novamente) com uma substituição monoalfabética simples.</p><br />
	
		<h5>Seção 25 da Divisão de Informações</h5><br />
	
		<p class="text-justify">A equipe de Ewing estavase expandindo e em novembro de 1914 havia se mudado para a Sala 40 do Prédio Velho do Almirantado, onde ao lado havia uma sala de descanso onde decifradores com excesso de trabalho podiam descansar. Então, foi oficialmente designada Seção 25 da Divisão de Informações ou ID25, mas o nome Sala 40 permaneceu.</p>
		<p class="text-justify">Em dezembro de 1914, os decifradores da Sala 40, descobriram um ataque que seria feito pelos alemães, eles prepararam uma defesa com o objetivo de encotrar a flotilha alemã e engajá-la em seu retorno, o alto-comando aceitou o risco de mortes de civis, para não permitir que os alemães soubessem que tinham decifrado o código. </p>
		<p class="text-justify">Em janeiro de 1915 a marinha britânica teve nova oportunidade, dessa vez conseguiu interceptar os atacantes e afundar um cruzador, além de avariar outros 2. A reputação da Sala 40 estava assegurada.</p>
		<p class="text-justify">Após esse evento, em fevereiro do mesmo ano, os alemães mudaram o código, mas agora os decifradores conheciam as palavras codificadas e a nova cifra foi resolvida.</p>
		<p class="text-justify">No ano seguinte, a marinha alemã mudava o código toda noite, mas os decifradores da Sala 40 ficaram tão bons que, às vezes, conseguiam descobrir o novo código as duas da madrugada.</p><br />
	
