        <p class="text-justify">Certamente os segredos escondidos nas cifras usadas por milênios e que até hoje se faz presente nos tempos modernos, tomado por uma forma digital com uso de dispositivos
            tecnológicos, onde os códigos secretos são ainda mais complexos, mas não infalíveis de acordo com Edgar Allan Poe, "Pode se afirmar com vigor, que o engenho humano não consegue 
            engenhar uma cifra que o engenho humano não consiga resolver".</p>
        <p class="text-justify">Ao longo da história, o que podemos observar é que as cifras foram e continuam sendo um grande trunfo no uso de esconder informações dos seus adversários. Os egípcios usaram por 
            centenas de anos esta técnica, passando por um dos grandes estrategistas da guerra como Julio Cesar que usou o método de criptografia durante a idade das trevas da Europa 
            passando de milênio a milênio Thomas Jefferson também adotou um sistema de criptografia que foi usada na guerra civil americana e que por sua vez relatos dizem que 
            Charles Wheeaststone e Charles Babage, o pioneiro do computador, dedicaram muito tempo a a compreender a criptografia.</p>
        <p class="text-justify">Tudo indica que a interpretação de decodificação também se fez presente na Grã-Bretanha com o uso do telégrafo, perpetuando aos EUA na primeira guerra mundial até a década de 1920, quando os o governo americano repudiou o uso da criptografia. Isso durou até quando os japoneses atacaram Pearl Harbor.</p>
        <p class="text-justify">Na Alemanha, surge então a máquina que fora nomeada como Enigma, tida como uma engenhoca que seria impossível de se decifrar, mas na verdade surge então três magistrais decodificadores poloneses que superaram as expectativas.</p>
        <p class="text-justify">Esses três poloneses que eram extraordinários no conceito de decodificação de cifras, compartilharam seus conhecimentos com os britânicos que fizeram bom uso desses conhecimentos na batalha de El Alamein.</p>
        <p class="text-justify">Em meio a tudo isso, surge o primeiro computador programável da época, chamado Colossos usado em Bletchley Park para decifrar o código de Lorenz utilizado pelo alto comando alemão na segunda guerra mundial.</p>
        <p class="text-justify">Já usando um tom mais estilizado, podemos destacar que os espiões também fizeram bom uso da codificação de mensagens, durante a guerra fria a criação de cifras se tornou algo muito comum, e tendo um toque de sofisticação, usava-se técnicas de programação das mensagens  afim de só um grupo de especialistas pudessem ser capazes de descodificar tal mensagem.</p>
        <p class="text-justify">E com o advento da tecnologia, surgem empresas muito importantes que conhecemos até hoje, como por exemplo a NSA (Agência de Segurança Nacional) dos EUA e o britânico GCHQ (Quartel-General de comunicação do Governo).</p>
        <p class="text-justify">Dentre todos esses métodos e técnicas já citadas, há quem diga que por milênios se usava a arte da codificação. Povos como os chineses usavam inscrições em tumbas, usadas não para esconder mensagens valiosas mas sim para louvar o trabalho dos escribas, mas como tudo muda ao longo da evolução da civilização.</p>        
