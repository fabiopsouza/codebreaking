        <p class="text-justify">Ao longo da história o que se ode ver é que as cifras foram e continuam sendo um grande trunfo no uso de esconder informações dos seus adversários os egípcios usaram por 
            centenas de anos esta técnica passando por um dos grande estrategista da guerra como Julio Cesar que usou o método de criptográfica durante a idade das trevas da Europa 
            passando de milênio a milênio Thomas Jefferson também adotou um sistema de criptografia que foi usada na guerra civil americana e que por sua vez relatos dizem que 
            Charles Wheeaststone e Charles Babage o pioneiro do computador dedicaram muito tempo a a compreender a criptografia.</p>
        <p class="text-justify">Tudo indica que a interpretação de decodificação também se fez presente na Grã-Bretanha com o uso do telegrafo perpetuando aos EUA na primeira guerra mundial até a década de 1920 quando os o governo americano repudiou o uso da criptográfica. Isso durou até quando os japoneses atacaram Pearl Harbor.</p>
        <p class="text-justify">Na Alemanha surge então a máquina que fora nomeada como Enigma que era tida como uma engenhoca que seria impossível de se decifrar, mas na verdade surge então três magistrais decodificadores poloneses que superaram as expectativas.</p>
        <p class="text-justify">Esses três poloneses que eram extraordinários no conceito de decodificação de cifras compartilharam seus conhecimentos com os britânicos que fizeram bom uso desses conhecimentos na batalha de El Alamein.</p>
        <p class="text-justify">Em meio a tudo isso surge o primeiro computador programável da época chamado colossos usado em Bletchley Park para decifrar o código de Lorenz usado pelo alto comando alemão na segunda guerra mundial.</p>
        <p class="text-justify">Já usando um tom mais estilizado podemos destacar que os espiões também fizeram bom uso da codificação de mensagens, durante a guerra fria a criação de cifras se tornou algo muito comum, e tendo um toque de sofisticação, usava-se técnicas de programação das mensagens  afim de só um grupo de especialistas pudessem ser capazes de descodificar tal mensagem.</p>
        <p class="text-justify">E com o advento da tecnologia aparecem empresas muito importantes que conhecemos até nos dias de hoje como por exemplo a NSA (Agência de Segurança Nacional) dos EUA e o britânico GCHQ (Quartel-General de comunicação do Governo).</p>
