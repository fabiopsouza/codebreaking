<div class="row">		
	<div class="col-12">  
        <h5 style="color: #ce3c41">Uma chave indecifrável</h5>
    </div>
	
	<div class="col-12 col-md-12">	
		<p class="text-justify">Após ajudar e projetar o M-94, Mauborgne desenvolveu uma chave indecifrável. Ele a criou enquanto testava um sistema de codificação automática, imaginado em 1918 por Gilbert Vernam, engenheiro da AT&T. Uma fita com o texto original passava pela máquina de codificação ao mesmo tempo em que outra fita contendo a chave. A mensagem, então seria codificada numa terceira fita, utilizando o que hoje chamaríamos de porta XOR. O segredo para decifrar a mensagem codificada, era passar novamente pela máquina utilizando a mesma chave. Mauborgne reconhecia que o ponto fraco do sistema de Vernam era que a chave girava o anel e percebeu que a solução seria uma fita-chave sem fim. </p>
	</div>	
</div>

<div class="row">	
	<div class="col-12">  
        <h5 style="color: #ce3c41">A cifra de Wheatstone</h5>
    </div>

	<div class="col-12 col-md-12">	
		<p class="text-justify">Em 1867 Wheatstone apresentou seu “Criptógrafo” na Exposição Universal de Paris. Semelhante a máquina de Wadsworth, com 26 letras no anel interno e 26 letras mais um espaço (totalizando 27 elementos) no anel externo, conhecido como “despacho”. Dois ponteiros como os de relógio eram ligados por engrenagens na proporção de 26:27. O ponteiro mais longo indicava as letras do anel externo, já o pequeno escolhia letras do anel interno. </p>
	</div>
