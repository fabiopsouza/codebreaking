<div class="row">		
	<div class="col-12">  
        <h5 style="color: #ce3c41">A Cifra de Wadsworth</h5>
    </div>
	
	<div class="col-12 col-md-12">	
		<p class="text-justify">O Coronel Decius Wadsworth, engenheiro e oficial da artilharia dos EUA, em 1817, desenvolveu um método mecânico de codificação similar ao de Alberti, compostos por dois discos. O disco externo tinha as 26 letras do alfabeto e os números 2 e 8. Eram gravados em pinos de latão que podiam ser montados em qualquer ordem. Já o disco interno era composto por 26 letras. Para que uma mensagem fosse codificada, acreditavam-se de que as letras do texto original eram soletreadas, uma de cada vez no disco interno, sendo equivalente ao disco externo para sua transmissão. </p>
	</div>	
</div>

<div class="row">		
	<div class="col-12">  
        <h5 style="color: #ce3c41">Joseph Mauborgne (1881 – 1971)</h5>
    </div>
	
	<div class="col-12 col-md-12">	
		<p class="text-justify">Joseph Mauborgne dedicou a carreira a aprimorar a criptografia militar americana. Foi o pioneiro a solucionar a cifra de Playfair, usada pelos britânicos como cifra de campanha. </p>
	</div>	
</div>

<div class="row">		
	<div class="col-12">  
        <h5 style="color: #ce3c41">Cilindro de Bazeries</h5>
    </div>
	
	<div class="col-12 col-md-12">	
		<p class="text-justify">Etienne Bazeries desenvolveu sua própria versão dos discos de códigos, as denominando Cilindro de Bazeries que fora utilizada pelo exército americano entre 1923 e 1942 e chamada de M-94. Uma medida proposta pelo capitão Parker Hitt. Hitt escreveu: “esse aparelho se baseia, até certo ponto, nas ideias do comandante Bazeries do exército Francês”. Ele simplesmente descolou os alfabetos embaralhados dos rotores do cilindro de Bazeries. Hitt também criou seu próprio cilindro, mas preferia o método das tiras. </p>
	</div>	
</div>

<div class="row">	
	<div class="col-12">  
        <h5 style="color: #ce3c41">Conversa em Código</h5>
    </div>

	<div class="col-12 col-md-12">	
		<p class="text-justify">As forças armadas americanas usavam idiomas nativos e complexos como códigos militar na primeira guerra Mundial. Um grupo de índios chamados de choctaw, do estado  de Oklahoma foram os pioneiros no uso de idiomas nativos americanos como código militar. As forças armadas americanas colocavam choctaws (grupo étnico muscogueano nativo da região sudeste dos Estados Unidos da América) em todas as companhias e puderam transmitir mensagens mesmo que houvesse escuta no rádio ou nas linhas telefônicas.</p>
