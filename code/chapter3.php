    </div>
</div>

<div class="row">
    <div class="col-12 col-md-8">
        <p class="text-justify">A Conspiração de Babington demonstrou que eram necessárias cifras mais sofisticadas. Seu desenvolvimento tinha começado no século anterior, quando, em 1467, o arquiteto e polímata italiano Leon Battista Alberti escreveu um tratado sobre criptoanalise que englobava a primeira tabela de frequências conhecidas. A tabela representava a fraqueza inerente à substituição monoalfabética. Alberti descobriu que, se usasse mais de um alfabeto de substituição e que os trocassem dificultaria e confundiria os decifradores. Com isso, descreveu o chamado disco de Alberti. O instrumento era constituído por dois discos de cobre com um eixo comum que giravam em relação ao outro. Na parte externa do circulo, Alberti inseriu as letras maiúsculas do alfabeto em cada célula, subtraindo “H”, “K” e “Y”. Na época, o alfabeto latino e italiano não possuía “J”, “U” e “W”, dando-lhe quatro células vazias, que fora preenchida com os números 1 a 4. No circulo interno, fora postas letras minúsculas em ordem aleatória. De forma geral, o disco vinha com um manual de utilização e com 336 expressões com valores numéricos gerados pelos números 1 a 4, codificados como letras minúsculas do disco.</p>
