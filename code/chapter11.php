    </div>
	
	<div class="col-12 col-md-12"> 
		<p class="text-justify">Em 1º de dezembro de 1941, a marinha japonesa mudou inesperadamente todos os seus sinais de chamada. Isso acontecia de seis em seis meses.</p><br>		
	</div>
</div><br />

<div class="row"> 
	<div class="col-12">
        <h5 style="color: #39CCCC">Queimem os livros de códigos</h5><br>		
    </div>
	
	<div class="col-12 col-md-12"> 
		<p class="text-justify">Em Honolulu, uma briga acirrada tinha começado com o FBI a respeito do grampo telegráfico, fazendo a marinha interromper sua vigilância do consulado japonês, mas só depois de ouvirem a instrução de queimar todos os livros de código e documentos secretos. A embaixada japonesa em Washington recebeu ordem de destruir todas as máquinas Roxas. Primeiro, as máquinas seriam desmontadas com uma chave de fenda. Depois, as peças teriam de ser achatadas com um martelo e dissolvidas em ácido fornecido pelo adido naval.</p><br>		
	</div>
</div><br />

<div class="row"> 
	<div class="col-12">
        <h5 style="color: #39CCCC">Declarações de guerra</h5><br>		
    </div>
	
	<div class="col-12 col-md-12"> 
		<p class="text-justify">A declaração de guerra do Japão chegou à sala do telégrafo do Ministério do Exterior japonês. Kameyama dividiu a notificação de cinco mil palavras em quatorze partes. Ele também cifrou um bilhete mais curto que avisava à embaixada de Washington que a resposta á declaração de Hull estava a caminho. A estação de monitoramento da ilha Brainbridge captou as treze primeiras partes e as enviou para o OP-20-G para serem decifradas. O SIS também as captou, mas as mensagens estavam embaralhadas, e o texto cifrado teve de ter passado novamente pela máquina Roxa da marinha. Os cifradores da embaixada japonesa também trabalhavam na notificação. Em Honolulu, a contraespionagem do exército ficou preocupada com o telefonema grampeado entre uma jornalista japonesa. As treze primeiras notificações circulavam como Magic. Quando as leu, Roosevelt disse simplesmente: “Isso significa guerra”. Com ele estava Harry Hopkins, administrador do programa de Empréstimos e Arrendamentos, quem aconselhou Roosevelt a dar o primeiro golpe. Enquanto a primeira onda de aviões japoneses decolava de porta-aviões no Pacífico, o embaixador Nomura ligou para o secretário Hull para requisitar uma reunião à uma da tarde.</p><br>		
		<p class="text-justify">Desconfiado da segurança dos misturadores telefônicos, o chefe do estado-maior George C. Marshall enviou um alerta em código para os generais comandantes do Havaí e o despacho para as Filipinas teria prioridade.</p><br>		
