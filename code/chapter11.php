        <h2 style="color: #39CCCC">Capítulo 11 - Os códigos navais japoneses</h2><br>		
    </div>

    <div class="col-12 col-md-12">        
        <p class="text-justify">A intensão dos japoneses era atacar os EUA. A informação era baseada nas mensagens que os criptólogos americanos e autoridades do governo decifravam, porém, a mudança constante dos códigos navais japoneses impossibilitava a leitura de onde aconteceria este ataque.</p>		
        <p class="text-justify">Embora o código roxo tivesse sido decifrado, o principal código JN-25 de comunicação de comando não havia sido. O SIS e o OP-20-G o atacaram com as tabuladoras IBM. O antigo código “S” japonês, furtado alguns anos antes, era atribuído aos grupos de números codificados de quatro algarismos. Zero era 0000, 1 era 0102, 2 era 0204, 3 era 0306 e assim sucessivamente. Com um método de diferenças os criptoanalistas constataram que 00102, 00204, 00306 e 00408 apareciam com frequência.</p>		
        <p class="text-justify">A delegação americana descobriu que os britânicos faziam progresso em outra parte do JN-25 no Extremo Oriente, em Bletchley Park. Em Cingapura, rapidamente os oficiais de ligação foram trocados entre o Escritório do Extremo Oriente e a unidade criptoanalítica da marinha americana na base Naval de Cavite, nas Filipinas. Porém, enquanto se faziam as alterações, os japoneses modificaram e atualizaram seus códigos e assim começavam os preparativos para o ataque a Pearl Harbor.</p><br>		
    </div>
</div>

<div class="row">
    <div class="col-12">
        <h5 style="color: #39CCCC">A unidade de Informações em Combate</h5><br>		
    </div>

    <div class="col-12 col-md-12">        
        <p class="text-justify">O capitão-de-corveta Joseph J. Rochefort foi enviado para assumir o comando da Unidade de Rádio do 14º Distrito Naval, no Havaí, em 1941. Rochefort era especialista em radiocomunicação, criptoanalista e falante de japonês. Sua unidade foi rebatizada de Unidade de Informações em Combate. A Unidade de Informações em Combate ouviu mensagens pelo rádio que a levou a acreditar que, depois da queda da França, o Japão pretendia ocupar as colônias francesas na Indochina – Vietnã, Laos e Camboja. Mensagens foram enviadas para certos almirantes que tiveram de ser redirecionadas. As mensagens interceptadas no código Vermelho ou Roxo pelos postos americanos de monitoramento eram enviadas pelo rádio a Washington depois de recifradas. Depois de decifrá-las, enviavam as chaves diárias para os criptoanalistas de Washington lerem as mensagens de outras estações de monitoramento. Também foram tomadas providências para grampear o telefone e as linhas telegráficas do consulado japonês no Havaí. As mensagens de nível mais alto de segurança passaram por uma série de cifras de transposição antes de serem codificadas na máquina Roxa. No entanto, como os códigos de transposição anteriores tinham sido descobertos, decodificar a mensagem era uma tarefa relativamente simples, apesar de laboriosa.</p><br>		
    </div>
</div>

<div class="row"> 
	<div class="col-12">
        <h5 style="color: #39CCCC">Sensação de Segurança</h5><br>		
    </div>
	
	<div class="col-12 col-md-3"> 
		<img src="view/static/images/MacArthu.png"  alt="MacArthu" class="img-fluid img-thumbnail"><br />			    
		
		<small>General Douglas MacArthur</small>
	</div>
	
	<div class="col-12 col-md-9"> 
		<p class="text-justify">A segurança americana não era tão fortificada. Os textos decifrados das mensagens japonesas, dando até o número da mensagem eram transmitidos em despachos. Em 28 de abril de 1940, a embaixada alemã em Washington avisou Berlim que o Departamento do Estado tinha as chaves dos códigos japoneses e a notícia da decifração foi passada a Tókio. Os criptoanalistas americanos temiam que os códigos fossem alterados, pois teriam de começar tudo novamente. Porém, as mensagens transmitidas pelos japoneses simplesmente deram instruções para aumentar a segurança. A segurança americana não era muito melhor. Um de seus maiores problemas era encontrar um número suficiente de tradutores para que fossem aprovados pelo sistema rigoroso.  Em 1941, quando as relações nipo-americanas chegavam ao ponto de rompimento, o SIS e o OP-20-G entregavam 75 mensagens por dia, algumas até com 15 páginas datilografadas. Em 16 de outubro de 1941, Hideki Tojo se tornou primeiro-ministro do Japão. Uma das primeiras coisas que fez no cargo foi convocar o chefe do telégrafo Kazuji Kameyama e lhe perguntar se as comunicações diplomáticas eram seguras.</p><br>		
	</div>	
</div>

<div class="row"> 
	<div class="col-12">
        <h5 style="color: #39CCCC">Preparativos para a Guerra</h5><br>		
    </div>
	
	<div class="col-12 col-md-12"> 
		<p class="text-justify">O plano do ataque a Pearl Harbor estava em andamento, e a força designada começou a se reunir junto à remota ilha de Etoforu, no arquipélago das Curilas, ao norte do Japão. A estação americana de monitoramento na ilha Brainbridge, em Puget Sound, recebeu uma mensagem que instruía a embaixada do Japão em Washington a escutar o boletim diário de notícias japonesas na transmissão de ondas curtas. A expressão “vento leste, chuva”, significava que as relações nipo-americanas seriam rompidas e que todos os códigos deveriam ser destruídos. “Vento norte, nublado” significava que houvera uma ruptura nas relações com a união Soviética. “Vento oeste, limpo” significava que o mesmo acontecera com os britânicos. Os embaixadores Nomura e Saburo Kurusu apresentaram um ultimato ao secretário de estado de Cordell Hull. Trinta e dois navios da força de ataque partiram das Curilas em 25 de novembro, sob escrito silêncio de rádio. Kurusu telefonou em código para Tóquio e avisou que as negociações não chegariam a lugar nenhum. É claro que os americanos tinham grampeado a linha, e o código era transparente. </p><br>		
	</div>
</div><br />

<div class="row"> 
